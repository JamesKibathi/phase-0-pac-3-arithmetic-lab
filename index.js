function add(a,b){
 let sum = a+b;
 return sum;
}

function subtract(a,b){
    let result=a-b;
    return result;

}
function multiply(a,b){
    let product=a*b;
    return product;

}
function divide(a,b){
    let division=a/b;
    return division;

}

function increment(n){
    return n+1;
}

function decrement(n){
    return n-1;
}

function makeInt(n){
   let res=parseInt(n,10);
   return res;
}

function preserveDecimal(n){
   return parseFloat(n);
}